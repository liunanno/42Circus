#include "ft_printf.h"

int	ft_putptr_pf(unsigned long long ptr, int i)
{
	if (ptr >= 16)
	{
		i++;
		i = ft_putptr_pf(ptr / 16, i);
	}
	ft_putchar_pf("0123456789abcdef"[ptr % 16]);
	return (i);
}
