#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	int	i;
	int	j;

	if (!dst || !src)
		return (NULL);
	if (dst > src)
	{
		i = (int)len - 1;
		while (i >= 0)
		{
			*(char *)(dst + i) = *(char *)(src + i);
			i--;
		}
	}
	else
	{
		i = (int)len;
		j = 0;
		while (j < i)
		{
			*(char *)(dst + j) = *(char *)(src + j);
			j++;
		}
	}
	return (dst);
}
