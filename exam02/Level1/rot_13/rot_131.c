#include <unistd.h>

void	rot_13(char *str)
{
	while(*str)
	{
		if ((*str >= 'N' && *str <= 'Z') || (*str >= 'n' && *str <= 'z'))
			*str -= 13;
		else if ((*str >= 'A' && *str <= 'M') || (*str >= 'a' && *str <= 'm'))
			*str += 13;
		write(1, str, 1);
		str++;
	}
}

int	main(int ac, char **av)
{
	if (ac == 2)
		rot_13(av[1]);
	write(1, "\n", 1);
	return (0);
}
