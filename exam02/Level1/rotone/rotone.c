#include <unistd.h>

void rotone(char *str)
{
	while (*str)
	{
		if (*str == 'z' || *str == 'Z')
			*str -= 25;
		else if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
			*str += 1;
		write(1, str, 1);
		str ++;
	}
}

int main(int ac, char **av)
{
	if (ac == 2)
		rotone(av[1]);
	write(1, "\n", 1);
	return (0);
}
