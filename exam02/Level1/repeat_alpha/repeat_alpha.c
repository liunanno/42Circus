#include <unistd.h>

int main(int argc, char **argv)
{
    int i = 0;
    
    if (argc == 2)
    {
        while (argv[1][i])
        {
            if ((argv[1][i] >= 97 && argv[1][i] <= 122) || (argv[1][i] >= 65 && argv[1][i] <= 90))
            {
                char c = argv[1][i];
                if (c >= 97 && c <= 122)
                    c -= 32;
                int j = 0;
                while (j <= (c - 65))
                {
                    write(1, &argv[1][i], 1);
                    j++;
                }
            }
            else
                write(1, &argv[1][i], 1);
            i++;
        }
    }
    write(1, "\n", 1);
    return (0);
}
