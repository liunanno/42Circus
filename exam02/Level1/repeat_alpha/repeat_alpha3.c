#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int j;
	char c;

	i = 0;
	j = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			c = argv[1][i];
			if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
			{
				if (c >= 'a' && c <= 'z')
					c -= 32;
				j = 0;
				while (j <= (c - 'A'))
				{
					write(1, &argv[1][i], 1);
					j++;
				}
			}
			else
				write(1, &argv[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
