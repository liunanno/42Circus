char	*ft_strrchr(char *str, char c)
{
	char	*last_ptr;

	*last_ptr = NULL;
	while (*str != '\0')
	{
		if (*str == c)
		{
			last_ptr = str;
		}
		str++;
	}
	return (last_ptr);
}
